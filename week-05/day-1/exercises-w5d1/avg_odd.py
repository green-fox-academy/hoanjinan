# Given a list with the following items: 1, 3, -2, -4, -7, -3, -8, 12, 19, 6, 9, 10, 14
# Calculate the average of the odd numbers.
from functools import reduce

numbers = [1, 3, -2, -4, -7, -3, -8, 12, 19, 6, 9, 10, 14]

filtered_nums = list(filter(lambda num: num % 2 != 0, numbers))

avg_odd = reduce(lambda a, b: a + b, filtered_nums) / len(filtered_nums)

